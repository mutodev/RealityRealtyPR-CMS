	function tdate( $format , $date = null, $inputTimezone = 'UTC', $outputTimezone = 'UTC'){

    	global $DATE_SUBSTITUTIONS;
        global $DATES;

	    if( isset( $DATES[ $format] ) ){
	        $format = $DATES[ $format ];
	    }

        if ($date == null) {
            $date = new DateTime();
        }
        else if (is_numeric($date)) {
            $date = new DateTime("@$date");
        }
        else if (is_string($date)) {
            $date = new DateTime($date, new DateTimeZone($inputTimezone));
        }

        //Set output timezone
        $date->setTimezone(new DateTimeZone($outputTimezone));

	    $result = array();
	    $len    = strlen( $format );
	    for( $i = 0; $i < $len ; $i++ ){
	        $char = $format{ $i };

	        $substitution = isset( $DATE_SUBSTITUTIONS[ $char] ) ? $DATE_SUBSTITUTIONS[ $char] : false;

	        if( $substitution ){
	            $value_key    = $substitution['value'];
	            $value        = $date->format($value_key);

	            //Get the translated value
	            if( isset($substitution["substitutions"][ $value ] ) ){
	                $tvalue = $substitution["substitutions"][ $value ];
	            }else{
    	            $tvalue = $date->format($char);
	            }

	            $result[] = $tvalue;

	        }else{
	            $result[] = $date->format($char);
	        }

	    }

        return implode("", $result );

	}

